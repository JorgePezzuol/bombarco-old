<?php

class TransacoesController extends GxController {

public function filters() {
	return array(
			'accessControl', 
			);
}

public function accessRules() {
	return array(
			array('allow', 
				'actions'=>array('minicreate', 'create','update'),
				'users'=>array('@'),
				),
			array('allow', 
				'actions'=>array('admin','delete','view'),
				'expression' => '$user->isAdmin()'
				),
			array('deny', 
				'users'=>array('*'),
				),
			);
}

	public function actionView($id) {
		$this->render('view', array(
			'model' => $this->loadModel($id, 'Transacoes'),
		));
	}

	public function actionCreate() {
		$model = new Transacoes;


		if (isset($_POST['Transacoes'])) {
			$model->setAttributes($_POST['Transacoes']);

			if ($model->save()) {
				if (Yii::app()->getRequest()->getIsAjaxRequest())
					Yii::app()->end();
				else
					$this->redirect(array('view', 'id' => $model->id));
			}
		}

		$this->render('create', array( 'model' => $model));
	}

	public function actionUpdate($id) {
		$model = $this->loadModel($id, 'Transacoes');


		if (isset($_POST['Transacoes'])) {
			$model->setAttributes($_POST['Transacoes']);

			if ($model->save()) {
				$this->redirect(array('view', 'id' => $model->id));
			}
		}

		$this->render('update', array(
				'model' => $model,
				));
	}

	public function actionDelete($id) {
		if (Yii::app()->getRequest()->getIsPostRequest()) {
			$this->loadModel($id, 'Transacoes')->delete();

			if (!Yii::app()->getRequest()->getIsAjaxRequest())
				$this->redirect(array('admin'));
		} else
			throw new CHttpException(400, Yii::t('app', 'Your request is invalid.'));
	}

	public function actionAdmin() {
		$model = new Transacoes('search');
		$model->unsetAttributes();

		if (isset($_GET['Transacoes']))
			$model->setAttributes($_GET['Transacoes']);

		$this->render('admin', array(
			'model' => $model,
		));
	}

}